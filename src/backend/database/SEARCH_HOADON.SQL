CREATE PROCEDURE HOADON_SearchByMaHD
    @MaHD INT
AS
BEGIN
SELECT * FROM HOADON
WHERE HOADON.MAHD = @MAHD;
END;
GO

CREATE PROCEDURE HOADON_SearchByMaKH
    @MaKH INT
AS
BEGIN
SELECT * FROM HOADON WHERE HOADON.MAKH = @MAKH;
END;
GO 

CREATE PROCEDURE HOADON_SearchByDate
    @DATE DATE
AS
BEGIN
SELECT * FROM HOADON
WHERE DAY(HOADON.DATE) = DAY(@DATE) AND
      MONTH(HOADON.DATE) = MONTH(@DATE) AND
      YEAR(HOADON.DATE) = YEAR(@DATE);
END
GO

CREATE PROCEDURE HOADON_SearchByMonth
    @DATE DATE
AS
BEGIN
SELECT * FROM HOADON
WHERE MONTH(HOADON.DATE) = MONTH(@DATE)
AND   YEAR(HOADON.DATE) = YEAR(@DATE);
END;
GO

CREATE PROCEDURE HOADON_SearchByYear
    @DATE DATE
AS
BEGIN
SELECT * FROM HOADON WHERE YEAR(HOADON.DATE) = YEAR(@DATE);
END;


